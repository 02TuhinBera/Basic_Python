#It will print 0 to 9 , because it's initial value is 0.

for i in range(10):
    print(i)
    
# It will print 1 to 9, because it's initial value is 1.
    
for i in range(1,10):
    print(i)
    
# Step-size example
for i in range(2, 10, 2):
    print(i)                    # Here 2 is the step-size.
    