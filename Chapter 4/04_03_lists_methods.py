l1=[1, 61, 23, 99, 45, 63, 85]
print(l1)

l1.sort() #Sorts the list
print(l1)
l1.reverse() #Reverses the list
print(l1)
l1.append(47) #Adds the end of the list
print(l1)
l1.insert(0, 42) #Inserts 42 at the index 0
print(l1)
l1.pop(2) #Remove an element at index 2
print(l1)
l1.remove(99) #remove 99 from the list
print(l1)



